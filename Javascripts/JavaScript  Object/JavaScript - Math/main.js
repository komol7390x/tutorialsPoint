// JavaScript Math Asosiy Metodlari Misollari

// 1. abs() — modul (musbat qiymat)
console.log('abs(-5) =', Math.abs(-5));  // 5

// 2. ceil() — yuqoriga yaxlitlash
console.log('ceil(4.2) =', Math.ceil(4.2)); // 5

// 3. floor() — pastga yaxlitlash
console.log('floor(4.8) =', Math.floor(4.8)); // 4

// 4. round() — eng yaqin butun songa yaxlitlash
console.log('round(4.5) =', Math.round(4.5)); // 5

// 5. max() — eng katta qiymatni topish
console.log('max(2, 5, 1) =', Math.max(2, 5, 1)); // 5

// 6. min() — eng kichik qiymatni topish
console.log('min(2, 5, 1) =', Math.min(2, 5, 1)); // 1

// 7. pow() — daraja (exponentiation)
console.log('pow(3, 2) =', Math.pow(3, 2)); // 9

// 8. random() — 0 va 1 orasida tasodifiy son
console.log('random() =', Math.random()); // Misol: 0.7321 (har safar farq qiladi)

// 9. sqrt() — kvadrat ildiz
console.log('sqrt(16) =', Math.sqrt(16)); // 4

// 10. sin() — sinus (radianlarda)
console.log('sin(Math.PI / 2) =', Math.sin(Math.PI / 2)); // 1

// 11. cos() — kosinus (radianlarda)
console.log('cos(0) =', Math.cos(0)); // 1

// 12. trunc() — butun qismini olish (kasrni tashlab yuborish)
console.log('trunc(4.9) =', Math.trunc(4.9)); // 4
console.log('trunc(-4.9) =', Math.trunc(-4.9)); // -4
